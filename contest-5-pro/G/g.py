import os
import sys

path = os.path.dirname(os.path.abspath(__file__))
sys.stdin = open(path + "/input1.txt", "r")


def solve(n: int, t: list[list[int]]) -> int:
    """За билетами на премьеру нового мюзикла выстроилась очередь из N человек,
    каждый из которых хочет купить 1 билет. На всю очередь работала только одна
    касса, поэтому продажа билетов шла очень медленно, приводя «постояльцев»
    очереди в отчаяние. Самые сообразительные быстро заметили, что, как правило,
    несколько билетов в одни руки кассир продаёт быстрее, чем когда эти же билеты
    продаются по одному. Поэтому они предложили нескольким подряд стоящим людям
    отдавать деньги первому из них, чтобы он купил билеты на всех.

    Однако для борьбы со спекулянтами кассир продавала не более 3-х билетов в одни руки,
    поэтому договориться таким образом между собой могли лишь 2 или 3 подряд стоящих человека.

    Известно, что на продажу i-му человеку из очереди одного билета кассир тратит Ai секунд,
    на продажу двух билетов — Bi секунд,
    трех билетов — Ci секунд.
    
    Напишите программу, которая подсчитает минимальное время, за которое могли быть обслужены все покупатели.

    Обратите внимание, что билеты на группу объединившихся людей всегда покупает первый из них.
    Также никто в целях ускорения не покупает лишних билетов (то есть билетов, которые никому не нужны).

    ### Решение:
    Динамическое программирование.
    Сохраняем min время обслуживания всех до i-го покупателя (влючительно).
    """

    dp = [0] * (n + 1)
    dp[0] = 0

    if n > 0:
        # для первого - сам купит 1 билет
        dp[1] = t[1][0]

    if n > 1:
        # для второго:
        # - либо каждый сам купит по 1 билету (t[1][0] + t[2][0])
        # - либо первый купит сразу 2 билета (t[1][1])
        dp[2] = min(t[1][0] + t[2][0], t[1][1])

    for i in range(3, n + 1):
        dp[i] = min(
            dp[i - 1] + t[i][0],  # сам купит 1 билет
            dp[i - 2] + t[i - 1][1],  # предыдущий купит два билета
            dp[i - 3] + t[i - 2][2],  # пред-предыдущий купит три билета
        )
    return dp[n]


N = int(input())
timing = [[0, 0, 0]] * (N + 1)
for i in range(1, N + 1):
    timing[i] = list(map(int, input().split()))

print(solve(N, timing))
